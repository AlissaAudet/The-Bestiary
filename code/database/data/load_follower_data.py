def load_followers():
    connection = get_db_connection()
    cursor = connection.cursor()

    try:
        cursor.executemany("""
            INSERT INTO Followers (
                follower_uid, following_uid, follow_date
            ) VALUES (%s, %s, %s)
        """, data)
        connection.commit()
        print("Follower data inserted successfully.")
    except Exception as e:
        print(f"Error inserting follower_data: {e}")
        connection.rollback()
    finally:
        cursor.close()
        connection.close()

